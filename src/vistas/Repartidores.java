    public void obtenerRepartidores() {
        GridBagConstraints constraints = new GridBagConstraints();
        constraints.fill = GridBagConstraints.VERTICAL;

        jPanelContenedor.removeAll();
        jScrollPane1.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);

        SocketHandler.getOut().println(Mensajes.PETICION_MOSTRAR_REPARTIDORES + "--" + UsuarioActual.getId());
        try {
            String received = SocketHandler.getIn().readLine();
            String[] args = received.split("--");
            int numRepartidores = Integer.parseInt(args[1]);

            try {
                for (int i = 0; i < numRepartidores; i++) {
                    constraints.gridx = x;
                    constraints.gridy = y;

                    constraints.gridwidth = 1;

                    constraints.insets = new Insets(5, 5, 5, 5);
                    received = SocketHandler.getIn().readLine();
                    args = received.split("--");
                    String flag = args[0];
                    JPanelRepartidor repartidor = new JPanelRepartidor(this);
                    repartidor.setIdRepartidor(Integer.parseInt(args[1]));
                    repartidor.getjLabelNombre().setText(args[2]);
                    repartidor.getjLabelNombre().setAlignmentX(CENTER_ALIGNMENT);
                    repartidor.getjLabelDni().setText(args[5]);
                    repartidor.getjLabelEmail().setText(args[4]);
                    jPanelContenedor.add(repartidor, constraints);
                    jPanelContenedor.validate();
                    repartidor.setVisible(true);
                    repartidor.setSize(675, 100);
                    jPanelContenedor.repaint();
                    y++;
                }

            } catch (IOException ex) {
                Logger.getLogger(Repartidores.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (IOException ex) {
            Logger.getLogger(Repartidores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

