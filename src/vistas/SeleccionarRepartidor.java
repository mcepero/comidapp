    public void anadirRepartidores() {
        try {
            SocketHandler.getOut().println(Mensajes.PETICION_MOSTRAR_REPARTIDORES + "--" + UsuarioActual.getId());

            String received = SocketHandler.getIn().readLine();
            String[] args = received.split("--");
            int numRepartidores = Integer.parseInt(args[1]);

            for (int i = 0; i < numRepartidores; i++) {
                received = SocketHandler.getIn().readLine();
                args = received.split("--");

                Object[] row = {args[1], args[2], args[5]};

                DefaultTableModel model = (DefaultTableModel) jTableRepartidores.getModel();

                model.addRow(row);
            }
        } catch (IOException ex) {
            Logger.getLogger(SeleccionarRepartidor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

